def cmd_test(_: argparse.Namespace) -> int:
    suite = unittest.defaultTestLoader.loadTestsFromTestCase(ArchitectureMapTests)
    result = unittest.TextTestRunner(verbosity=2).run(suite)
    return 0 if result.wasSuccessful() else 1


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest="command", required=True)

    extract = subparsers.add_parser("extract")
    extract.add_argument("--base", required=True)
    extract.add_argument("--head", required=True)
    extract.add_argument("--out", required=True)
    extract.set_defaults(func=cmd_extract)

    render_docs_parser = subparsers.add_parser("render-docs")
    render_docs_parser.add_argument("--facts", required=True)
    render_docs_parser.add_argument("--output-root", default=str(REPO_ROOT))
    render_docs_parser.set_defaults(func=cmd_render_docs)

    render_pr = subparsers.add_parser("render-pr-section")
    render_pr.add_argument("--facts", required=True)
    render_pr.add_argument("--out", required=True)
    render_pr.set_defaults(func=cmd_render_pr_section)

    write_shards = subparsers.add_parser("write-owner-shards")
    write_shards.add_argument("--facts", required=True)
    write_shards.add_argument("--output-root", default=str(REPO_ROOT))
    write_shards.add_argument("--shards-dir", default=".architecture-map/owners")
    write_shards.add_argument("--matrix-out")
    write_shards.set_defaults(func=cmd_write_owner_shards)

    update_body = subparsers.add_parser("update-pr-body")
    update_body.add_argument("--body", required=True)
    update_body.add_argument("--section", required=True)
    update_body.add_argument("--out", required=True)
    update_body.set_defaults(func=cmd_update_pr_body)

    apply = subparsers.add_parser("apply")
    apply.add_argument("--base", required=True)
    apply.add_argument("--head", required=True)
    apply.add_argument("--output-root", default=str(REPO_ROOT))
    apply.add_argument("--facts-out")
    apply.add_argument("--pr-section-out")
    apply.add_argument("--shards-out")
    apply.add_argument("--owner-matrix-out")
    apply.set_defaults(func=cmd_apply)

    test = subparsers.add_parser("test")
    test.set_defaults(func=cmd_test)
    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    return args.func(args)


if __name__ == "__main__":
    sys.exit(main())
