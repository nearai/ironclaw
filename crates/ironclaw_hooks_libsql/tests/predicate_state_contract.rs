//! Durable [`LibSqlPredicateStateBackend`] verified against the shared
//! `PredicateStateBackend` contract suite from `ironclaw_hooks`
//! (durable-backend PR 3/4), plus libSQL-specific adversarial tests
//! (concurrent writers, cross-host replay, LRU + per-key cap under pressure).
//!
//! # Why `harness = false` + a hand-rolled serial runner
//!
//! This test binary opts out of the default libtest harness (see the
//! `[[test]]` entry in `Cargo.toml`). Several cases fill a single key all the
//! way to `MAX_SAMPLES_PER_KEY` (4 096) — each step is a fresh
//! connect / `BEGIN IMMEDIATE` / `COMMIT` cycle, because the backend opens a
//! connection per operation (no pool). Running several of those 4 096-step
//! fill loops *concurrently* against the replication-enabled libSQL build
//! intermittently trips `SQLITE_MISUSE` ("bad parameter or other API misuse").
//! Single-threaded, every case passes deterministically.
//!
//! The default harness offers no in-crate knob to cap parallelism for one test
//! binary, and the fail-closed contract case is generated by a cross-crate
//! macro we cannot gate. So we own the runner: cases run **serially** by
//! default (one heavy fill at a time), and the cases that intentionally
//! exercise concurrency get their own multi-thread Tokio runtime. The
//! `contract-tests` feature on the `ironclaw_hooks` dev-dependency exposes the
//! `predicate_state::contract` functions we call directly here instead of
//! through the `predicate_backend_contract_test!` macro.

