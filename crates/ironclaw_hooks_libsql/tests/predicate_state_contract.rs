/// Build a fresh, migrated libSQL-backed backend over a private temp-file
/// database. Returns the backend plus the `TempDir` guard (kept alive by the
/// caller for the duration of the test).
async fn fresh_backend() -> (LibSqlPredicateStateBackend, TempDir) {
    let dir = tempfile::tempdir().expect("tempdir");
    let path = dir.path().join("predicate_state.db");
    let db = Arc::new(
        libsql::Builder::new_local(path.to_string_lossy().to_string())
            .build()
            .await
            .expect("build libsql db"),
    );
    let backend = LibSqlPredicateStateBackend::new(db);
    backend.run_migrations().await.expect("migrate");
    (backend, dir)
}

/// Synchronous factory for the contract functions (which take `Fn() -> B`).
/// Builds the async-constructed backend on a dedicated current-thread runtime
/// on a fresh OS thread — avoids "cannot start a runtime from within a runtime"
/// since the caller is already inside a runtime via [`run`]. Leaks the
/// `TempDir` into the process so the db file outlives the contract body; the
/// test process is short-lived so a handful of leaked temp dirs is fine.
fn contract_factory() -> LibSqlPredicateStateBackend {
    std::thread::scope(|s| {
        s.spawn(|| {
            let rt = tokio::runtime::Builder::new_current_thread()
                .enable_all()
                .build()
                .expect("build current-thread runtime");
            rt.block_on(async {
                let (backend, dir) = fresh_backend().await;
                Box::leak(Box::new(dir));
                backend
            })
        })
        .join()
        .expect("setup thread joined")
    })
}

