/// Which Tokio runtime flavor a case needs.
enum Rt {
    /// Single-threaded current-thread runtime (the default; serialises heavy
    /// fill loops so at most one runs at a time across the binary).
    Current,
    /// Multi-thread runtime for cases that intentionally exercise concurrency.
    Multi { workers: usize },
}

/// Run one case on a freshly-built runtime, catching panics so a failure in one
/// case still lets the rest run and the final exit code reflects all failures.
fn run<F, Fut>(name: &str, rt: Rt, f: F) -> bool
where
    F: FnOnce() -> Fut,
    Fut: Future<Output = ()>,
{
    eprint!("test {name} ... ");
    let result = panic::catch_unwind(AssertUnwindSafe(|| match rt {
        Rt::Current => tokio::runtime::Builder::new_current_thread()
            .enable_all()
            .build()
            .expect("build current-thread runtime")
            .block_on(f()),
        Rt::Multi { workers } => tokio::runtime::Builder::new_multi_thread()
            .worker_threads(workers)
            .enable_all()
            .build()
            .expect("build multi-thread runtime")
            .block_on(f()),
    }));
    match result {
        Ok(()) => {
            eprintln!("ok");
            true
        }
        Err(_) => {
            eprintln!("FAILED");
            false
        }
    }
}

fn main() {
    let mut ok = true;

    // Shared PredicateStateBackend contract suite (mirrors the cases the
    // `predicate_backend_contract_test!` macro would generate). All run on a
    // single-threaded runtime; the overflow case is a heavy 4 096 fill.
    ok &= run(
        "contract::invocation_counts_within_window",
        Rt::Current,
        || contract::invocation_counts_within_window(contract_factory),
    );
    ok &= run(
        "contract::invocation_trims_outside_window",
        Rt::Current,
        || contract::invocation_trims_outside_window(contract_factory),
    );
    ok &= run("contract::value_sums_within_window", Rt::Current, || {
        contract::value_sums_within_window(contract_factory)
    });
    ok &= run("contract::tenant_isolation", Rt::Current, || {
        contract::tenant_isolation(contract_factory)
    });
    ok &= run(
        "contract::duplicate_event_id_is_noop_for_invocations",
        Rt::Current,
        || contract::duplicate_event_id_is_noop_for_invocations(contract_factory),
    );
    ok &= run(
        "contract::duplicate_event_id_is_noop_for_values",
        Rt::Current,
        || contract::duplicate_event_id_is_noop_for_values(contract_factory),
    );
    ok &= run(
        "contract::invocation_retains_entry_at_exact_window_cutoff",
        Rt::Current,
        || contract::invocation_retains_entry_at_exact_window_cutoff(contract_factory),
    );
    ok &= run(
        "contract::event_id_dedup_isolated_across_maps",
        Rt::Current,
        || contract::event_id_dedup_isolated_across_maps(contract_factory),
    );
    ok &= run(
        "contract::record_invocation_overflow_is_fail_closed",
        Rt::Current,
        || contract::record_invocation_overflow_is_fail_closed(contract_factory),
    );

    // libSQL-specific adversarial cases.
    ok &= run(
        "two_hosts_concurrent_writes_no_desync",
        Rt::Multi { workers: 4 },
        two_hosts_concurrent_writes_no_desync,
    );
    ok &= run(
        "cross_host_replay_is_deduped",
        Rt::Current,
        cross_host_replay_is_deduped,
    );
    ok &= run(
        "cross_host_replay_is_deduped_for_values",
        Rt::Current,
        cross_host_replay_is_deduped_for_values,
    );
    ok &= run(
        "per_key_sample_cap_fails_closed_under_pressure",
        Rt::Current,
        per_key_sample_cap_fails_closed_under_pressure,
    );
    ok &= run(
        "value_sum_cap_fails_closed",
        Rt::Current,
        value_sum_cap_fails_closed,
    );
    ok &= run(
        "per_tenant_quota_isolates_under_concurrent_pressure",
        Rt::Multi { workers: 4 },
        per_tenant_quota_isolates_under_concurrent_pressure,
    );
    ok &= run(
        "state_survives_restart",
        Rt::Current,
        state_survives_restart,
    );

    if !ok {
        eprintln!("\nsome predicate_state_contract cases FAILED");
        std::process::exit(1);
    }
    eprintln!("\nall predicate_state_contract cases ok");
}

