/// Which Tokio runtime flavor a case needs.
enum Rt {
    /// Single-threaded current-thread runtime (the default; serialises heavy
    /// fill loops so at most one runs at a time across the binary).
    Current,
    /// Multi-thread runtime for cases that intentionally exercise concurrency.
    Multi { workers: usize },
}

/// Run one case on a freshly-built runtime, catching panics so a failure in one
/// case still lets the rest run and the final exit code reflects all failures.
fn run<F, Fut>(name: &str, rt: Rt, f: F) -> bool
where
    F: FnOnce() -> Fut,
    Fut: Future<Output = ()>,
{
    eprint!("test {name} ... ");
    let result = panic::catch_unwind(AssertUnwindSafe(|| match rt {
        Rt::Current => tokio::runtime::Builder::new_current_thread()
            .enable_all()
            .build()
            .expect("build current-thread runtime")
            .block_on(f()),
        Rt::Multi { workers } => tokio::runtime::Builder::new_multi_thread()
            .worker_threads(workers)
            .enable_all()
            .build()
            .expect("build multi-thread runtime")
            .block_on(f()),
    }));
    match result {
        Ok(()) => {
            eprintln!("ok");
            true
        }
        Err(_) => {
            eprintln!("FAILED");
            false
        }
    }
}

